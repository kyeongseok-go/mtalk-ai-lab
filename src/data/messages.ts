export interface Attachment {
  type: 'image' | 'file' | 'link';
  name: string;
  url?: string;
}

export interface Message {
  id: string;
  senderId: string;
  content: string;
  timestamp: string;
  attachments?: Attachment[];
  reactions?: { emoji: string; count: number }[];
  isRead?: boolean;
  mentionIds?: string[];
}

export interface ChatRoom {
  id: string;
  name: string;
  description: string;
  memberIds: string[];
  messages: Message[];
  unreadCount: number;
  lastActivity: string;
  category: 'project' | 'team' | 'casual' | 'executive';
  isPinned?: boolean;
}

